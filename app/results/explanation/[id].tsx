import { StyleSheet, Text, View, TextInput, TouchableOpacity, ScrollView, Platform, KeyboardAvoidingView, Dimensions } from 'react-native';
import { useLocalSearchParams, useRouter, Stack } from 'expo-router';
import { useState, useRef, useEffect } from 'react';
import { Ionicons } from '@expo/vector-icons';
import { getHistory } from '@/services/storageService';
import { sendChatMessage } from '@/services/geminiService';
import Colors from '@/constants/Colors';
import { SafeAreaView } from 'react-native-safe-area-context';
import Animated, { FadeIn, FadeInUp, FadeInDown, SlideInRight, SlideInLeft } from 'react-native-reanimated';
import { LinearGradient } from 'expo-linear-gradient';

const { width } = Dimensions.get('window');

interface Message {
    id: string;
    text: string;
    role: 'user' | 'ai';
    timestamp: Date;
}

export default function ExplanationChatScreen() {
    const { id } = useLocalSearchParams();
    const router = useRouter();
    const [messages, setMessages] = useState<Message[]>([]);
    const [inputText, setInputText] = useState('');
    const [isTyping, setIsTyping] = useState(true);
    const [initialTypingDone, setInitialTypingDone] = useState(false);
    
    // Track the raw string explanation generated by Gemini
    const [fullExplanation, setFullExplanation] = useState('');
    const [displayedExplanation, setDisplayedExplanation] = useState('');

    const scrollViewRef = useRef<ScrollView>(null);

    useEffect(() => {
        loadInitialData();
    }, [id]);

    // Typewriter effect logic
    useEffect(() => {
        if (fullExplanation && !initialTypingDone) {
            let i = 0;
            const timer = setInterval(() => {
                setDisplayedExplanation(fullExplanation.substring(0, i));
                i++;
                if (i > fullExplanation.length) {
                    clearInterval(timer);
                    setInitialTypingDone(true);
                    setIsTyping(false);
                    // Add the completed message to the chat history
                    setMessages([{
                        id: 'initial',
                        text: fullExplanation,
                        role: 'ai',
                        timestamp: new Date()
                    }]);
                }
            }, 10); // typing speed

            return () => clearInterval(timer);
        }
    }, [fullExplanation, initialTypingDone]);

    const loadInitialData = async () => {
        try {
            const history = await getHistory();
            const item = history.find((h: any) => h.id === id);
            
            if (item && item.explanation) {
                setFullExplanation(item.explanation);
            } else {
                setFullExplanation("I couldn't find the explanation for this item.");
            }
        } catch (e) {
            console.error('Failed to load explanation', e);
            setFullExplanation("Error loading explanation.");
        }
    };

    const handleSend = async () => {
        if (!inputText.trim() || isTyping) return;

        const newUserMessage: Message = {
            id: Date.now().toString(),
            text: inputText.trim(),
            role: 'user',
            timestamp: new Date(),
        };

        setMessages(prev => [...prev, newUserMessage]);
        setInputText('');
        setIsTyping(true);

        try {
            // Include context of the original explanation plus conversation history
            const historyContext = messages.map(m => `${m.role === 'ai' ? 'Tutor' : 'Student'}: ${m.text}`).join('\n');
            const prompt = `
            You are a helpful AI tutor. 
            Original Topic/Explanation context: ${fullExplanation}
            
            Conversation history:
            ${historyContext}
            
            Student asks: ${newUserMessage.text}
            
            Please provide a clear, encouraging, and concise answer to the student's question based on the context.
            `;
            
            const responseText = await sendChatMessage(messages, prompt);
            
            const newAiMessage: Message = {
                id: (Date.now() + 1).toString(),
                text: responseText,
                role: 'ai',
                timestamp: new Date(),
            };

            setMessages(prev => [...prev, newAiMessage]);
        } catch (error) {
            console.error('Failed to get AI response', error);
            const errorMessage: Message = {
                id: (Date.now() + 1).toString(),
                text: "I'm sorry, I'm having trouble connecting right now. Please try again.",
                role: 'ai',
                timestamp: new Date(),
            };
            setMessages(prev => [...prev, errorMessage]);
        } finally {
            setIsTyping(false);
        }
    };

    return (
        <SafeAreaView style={styles.container} edges={['top', 'bottom']}>
            <Stack.Screen options={{ headerShown: false }} />
            
            {/* Background Gradient */}
            <LinearGradient
                colors={['#F0FDF4', '#FFFFFF']}
                style={StyleSheet.absoluteFillObject}
            />

            <View style={styles.header}>
                <TouchableOpacity onPress={() => router.back()} style={styles.iconBtn}>
                    <Ionicons name="arrow-back" size={24} color={Colors.text} />
                </TouchableOpacity>
                <View style={styles.headerTitleBox}>
                    <Text style={styles.headerTitle}>AI Tutor</Text>
                    <View style={styles.onlineStatus} />
                </View>
                <TouchableOpacity style={styles.iconBtn}>
                    <Ionicons name="ellipsis-horizontal" size={24} color={Colors.text} />
                </TouchableOpacity>
            </View>

            <KeyboardAvoidingView 
                style={styles.keyboardView} 
                behavior={Platform.OS === 'ios' ? 'padding' : undefined}
            >
                <ScrollView 
                    ref={scrollViewRef}
                    style={styles.chatArea}
                    contentContainerStyle={styles.chatContent}
                    onContentSizeChange={() => scrollViewRef.current?.scrollToEnd({ animated: true })}
                    showsVerticalScrollIndicator={false}
                >
                    {!initialTypingDone ? (
                        // Typewriter effect container
                        <Animated.View entering={FadeInUp} style={[styles.messageBubble, styles.aiBubbleBlob]}>
                            <Text style={styles.aiMessageText}>
                                {displayedExplanation}
                                <Animated.Text entering={FadeIn.delay(200)} style={{ color: Colors.primary }}>
                                    â–‹
                                </Animated.Text>
                            </Text>
                        </Animated.View>
                    ) : (
                        // Render full dialogue
                        messages.map((msg) => {
                            const isAi = msg.role === 'ai';
                            return (
                                <Animated.View 
                                    key={msg.id} 
                                    entering={isAi ? SlideInLeft.springify() : SlideInRight.springify()}
                                    style={[
                                        styles.messageRow, 
                                        isAi ? styles.aiRow : styles.userRow
                                    ]}
                                >
                                    {isAi && (
                                        <View style={styles.avatar}>
                                            <Ionicons name="school" size={16} color="#FFF" />
                                        </View>
                                    )}
                                    <View style={[
                                        styles.messageBubble, 
                                        isAi ? styles.aiBubbleBlob : styles.userBubbleBlob
                                    ]}>
                                        <Text style={isAi ? styles.aiMessageText : styles.userMessageText}>
                                            {msg.text}
                                        </Text>
                                    </View>
                                </Animated.View>
                            );
                        })
                    )}

                    {isTyping && initialTypingDone && (
                        <Animated.View entering={FadeInUp} style={[styles.messageRow, styles.aiRow]}>
                             <View style={styles.avatar}>
                                <Ionicons name="school" size={16} color="#FFF" />
                            </View>
                            <View style={[styles.messageBubble, styles.aiBubbleBlob, styles.typingBubbleBlob]}>
                                <ActivityIndicator size="small" color={Colors.primary} />
                            </View>
                        </Animated.View>
                    )}
                </ScrollView>

                {/* Input Area */}
                <Animated.View entering={FadeInDown.delay(300).springify()} style={styles.inputArea}>
                    <View style={styles.inputWrapper}>
                        <TextInput
                            style={styles.input}
                            placeholder={initialTypingDone ? "Ask a follow up question..." : "Tutor is typing..."}
                            placeholderTextColor="#94A3B8"
                            value={inputText}
                            onChangeText={setInputText}
                            multiline
                            maxLength={500}
                            editable={initialTypingDone && !isTyping}
                        />
                        <TouchableOpacity 
                            style={[
                                styles.sendBtn, 
                                (!inputText.trim() || !initialTypingDone || isTyping) && styles.sendBtnDisabled
                            ]}
                            onPress={handleSend}
                            disabled={!inputText.trim() || !initialTypingDone || isTyping}
                        >
                            <Ionicons name="paper-plane" size={20} color="#FFF" style={{ marginLeft: 2 }} />
                        </TouchableOpacity>
                    </View>
                </Animated.View>
            </KeyboardAvoidingView>
        </SafeAreaView>
    );
}

